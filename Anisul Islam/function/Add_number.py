from operator import add

def add(a,b):
    total = a + b
    print(total)

def add1(a,b,c):
    total = a + b + c
    print(total)

def sub1(a,b,c):
    total = a - b - c
    print(total)

add(10,20)
add(30,40)
add1(50,60,30)
sub1(0,-1,2)


